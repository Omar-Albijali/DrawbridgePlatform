export const LANGUAGE_STORAGE_KEY = 'drawbridge_lang';
export const supportedLanguages = ['en', 'ar'] as const;
export type SupportedLanguage = (typeof supportedLanguages)[number];

const isSupportedLanguage = (value: string | null | undefined): value is SupportedLanguage =>
  value === 'en' || value === 'ar';

function getStoredLanguage(): SupportedLanguage {
  if (typeof window === 'undefined') {
    return 'en';
  }

  const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return isSupportedLanguage(storedLanguage) ? storedLanguage : 'en';
}

function applyDocumentLanguage(languageValue: string): void {
  if (typeof document === 'undefined') {
    return;
  }

  const language: SupportedLanguage = languageValue.startsWith('ar') ? 'ar' : 'en';
  document.documentElement.lang = language;
  document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr';

  if (typeof window !== 'undefined') {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  }
}

